				    <p align="justify">
“That's when it first struck me that I wasn't alone. There were countless other engineers like me across the country who were lost in life,” recollects Adhitya. A few years later in life, it all came in handy. But back in 2011, the startup scene in India was not even half as glamorous and grand as it is today. After college, all his co-founders had either left for the US or opted for jobs. Adhitya had opted out of campus placement and after struggling to keep his startup afloat for a few months, he moved to Bengaluru. There, he joined an HBS grad (an Indian engineering graduate himself) and a few other IIT/IIM alums (all engineers again) in their endeavour to sell chai (Chai Point).</p>



<p align="justify">
After spending two years in Bengaluru, Adhitya realised: “Nobody, including the engineers themselves, have tried to understand what has happened to their lives ,” says Adhitya. He quit his job and sent an email to Darshan, a childhood friend in San Diego, California asking for funds for a backpacking trip across the country to figure out the life of Indian engineers. "Bank details, please," came the crisp response. Ṭhis was where it all started for this project on the Great Indian Obsession: engineering.The aim of this budget trip was to uncover every aspect of how engineering became such a big thing in the country and what the life of an engineer looks like. After he had come back from his revelatory first trip with Darshan's money, Adhitya knew he had an interesting story to tell. “I had by now done random chores to sustain myself. I worked as a junior artiste in a few films (one of which won a national award) and TV shows; I made websites for people, and sold T-shirts in engineering hostels. I wanted to bring out a book. Somebody wrote to us saying that we should consider Kickstarter,” says Adhitya. None of them had heard of Kickstarter or known of crowdfunding before.  A few days of research and they decided to go ahead with a campaign. They made a small video presentation and released it online to help people reach out to them with their stories and ideas</p>

<p align="justify">
The team was also in place- Darshan handles finance among various other things in the project. Suresh is one of the co-founders from Annanymous. A former data analyst now pursuing his MBA from Mumbai, he handles operations. And the plan was to put up a Kickstarter campaign for which they needed a foreign proxy. Kapil Sharma, Adhitya’s school friend who is currently a citizen of Australia came in to create a Kickstarter. “Books are not a big hit when it comes to crowdfunding. We were anyways going ahead with the project,” says Adhitya about why they put up a target of USD 14,000 for their project.
A well-prepared video is core to a successful crowdfunding campaign. They prepared the campaign video over two weeks coordinating between team members spread across Mumbai, Sydney and San Diego. “After going live, we were delighted to learn that we were made a Staff Pick project. Staff Pick projects are handpicked by the Kickstarter staff and have 80 per cent success rate of getting funded,” says Adhitya.</p>



<p align="justify">
The project mainly targeted Indians, who had no clue about Kickstarter or crowdfunding. “We tried to reach out to people outside our network via FB, email, Twitter, and LinkedIn, but in vain. Some Internet celebs were gracious enough to share the link to our campaign which also received some traction. We then decided to focus on our immediate social network and that did us wonders. But even after 25 days of extensive campaigning and five days remaining, we had managed to raise only around USD 5,000,” says Adhitya. And with Kickstarter, it is an ‘all or none’ game. Even if the campaign raised USD 13,999, it would be considered a failure.
Close to giving up, one evening the team saw somebody pledging USD 2,500. “That evening gave new life to the campaign and to everyone associated with it. Since then, all we did was inform our backers of this massive development . Over the next few days, we saw friends reaching out to friends who reached out to their friends. Everyone wanted to help us reach our target,” says Adhitya. For a book on Indian engineers, in characteristic fashion, the team raised more than 60 per cent of the funds in the last five days.
</p>


