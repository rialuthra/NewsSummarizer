"We go to toilet at least twice a day and pay Rs 5 each time. We are three people in our family and have to keep at least Rs 30 aside for the expense every day," said Mallappa Myadar, a resident of Myadar Oni. He said that the residents are mostly poor and work as labourers. "We have to spend at least Rs 1,000 to Rs 1,200 a month for using toilet. It's a huge burden on us, of which we can't even relieve ourselves."</p>

