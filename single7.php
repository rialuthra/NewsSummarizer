"We don't have an option. The only public toilet sees a huge rush in the morning hours because people need to go to work. After the toilet shuts at 9pm, we have to wait till the morning," said another resident Savitramma Sanshi.Appeals to the Hubballi-Dharwad Municipal Corporation (HDMC) to sanction funds for construction of individual toilets have fallen on deaf ears, according to the residents. "The government sanctions Rs 12,000 for constructing an individual toilet. We have applied for it two years ago but haven't got any response from the authority concerned," said Shantavva Navalagund.</p>
