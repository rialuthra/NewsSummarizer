At the time, Castaneda said Franco's obesity and related conditions, including diabetes, had made the operation impossible. Franco contacted the clinic after coming across one of their online ads. Before that, he had spent the past six years lying in bed due to his massive weight. Despite his weight loss, it cannot be guaranteed that complications will not appear. Franco's doctor is optimistic, saying they were "on the right path". The initial gastric bypass aims to reduce his current weight by 50 per cent, after which a second operation will be needed, said Castaneda.</p>
