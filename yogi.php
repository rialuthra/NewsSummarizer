Uttar Pradesh chief minister Yogi Adityanath has banned the use of plastic products, pan masala and tobacco on the premises of government offices and buildings in the state. "After detecting betel stains on the walls (of the government annexe building), the chief minister asked employees not to consume pan or pan masala during duty hours and ensure cleanliness," deputy chief minister Keshav Prasad Maurya told reporters in Lucknow. CM Yogi Adityanath ordered a swift action against the accused in a molestation case after the victims' family member tweeted to him seeking justice. Some drunk youths had barged into a house in Kanpur's Kalyanpur area on Holi and started molesting a woman and her daughter. When the woman's husband protested, the perpetrators assaulted him. The husband filed a case at the Kalyanpur police station. However, the complainant alleging that the police were "lackadaisical" in their probe and tweeted his grievance to the DGP and the Chief Minister's Office asking for their help in the case. Acting on the chief minister's order, three teams have been formed to nab the assaulters while the family has been provided security.</p>



<p align="justify">
First Cabinet Meeting. Uttar Pradesh chief Minister Yogi Adityanath presided over his first cabinet meeting this evening. The issues discussed in the meeting were that of farmers' loans, fixation of the new power tariff, Law and Order and measures to put the state's fiscal situation on track. BJP spokesperson said the chief minister had reiterated the need to restore rule of law in the state and address problems of the public. Cabinet minister Srikant Sharma added that the chief minister has directed officials to ensure quick clearance of files and to maintain a proper index to track their movement. The chief minister also allocated portfolios to his council of ministers. He retained the key ministries of Home and Revenue with himself.</p>

<p align="justify">
Two days after Yogi Aditya Nath and his 46 ministers assumed charge, the "Anti Romeo Squads" - the much talked-about promise in the party's poll manifesto - swung into action across the state. From Gorakhpur to Ghaziabad and Lucknow to Lalitpur, squads drawn from the city police fanned out to check markets, malls, schools, colleges, coaching centres and other spots to check incidents of eve-teasing. In Lucknow, two youths were arrested when they failed to explain the reason for their presence in front of a girls' school. Besides, five people were detained and over 900 spots were checked in the city. DGP Javeed Ahmed said instructions have been issued to chalk out a specific dedicated drill to "reclaim" public places from hooligans and anti-social elements and generate a feeling of security among the people in general and women in particular.</p>


<p align="justify">
Aditya Nath today directed police officials to prepare an action plan to shut slaughterhouses in the state. Meanwhile, Police and municipal authorities began to crack down on illegal slaughterhouses across the state with raids on Tuesday on many such units in most of the districts. A couple of abattoirs were sealed. Besides, there was an extensive drive to check the licence of meat shops. In the state capital, the Lucknow Municipal Corporation swung into action and sealed nine meat shops in Mahanagar and Aliganj. Along with possession of licences issued by LMC, the butcher shops will also be reviewed on the parameters of Pollution Act, ground water conservation guidelines and Food Safety act. As per LMC's estimate, along with 600 licensed meat shops, there are approximately 200-250 butcher shops that are running illegally. On Wednesday, the UP CM directed police officials to prepare action plan for closure of abattoirs in the state. He also ordered blanket ban on smuggling of cows.</p>


