				    <p align="justify">
Nothing could be more contrasting than India and England's form going into the five-match Test series, starting in Rajkot on Wednesday. While the hosts clean-sweeped their rivals in last two Test series, the Alastair Cook-led side lost against Bangladesh in a Test match.The Virat Kohli-led India would be elated looking at the recent happenings, but it would not be the case if they look a few years back.The last time India beat England in a Test series was way back in 2008, when the Mahendra Singh Dhoni-led team beat England 1-0 in a two-Test series.The two teams have played three Test series since then - in 2011, 2012 and 2014 and India lost all of them. In 2012, in fact, it was a home series for India.</p>





<p align="justify">
In-form side.
Four members of the victorious 2008 squad are in the side for the upcoming series - Gautam Gambhir, Murali Vijay, Ishant Sharma and Amit Mishra. The quartet are expected to play a significant role if India would hope to negate history and prevail over England.Apart from the four players, the biggest positives for India are the menacing form of spinners Ravichandran Ashwin and Ravindra Jadeja along with the presence of in-form captain Kohli.Considering England's weakness against spin, which again came to the fore against Bangladesh, India might be waiting eagerly to exact revenge.Mishra was India's top bowler, with 15 wickets, in the ODI series against New Zealand, while Ashwin recently became the second-fastest bowler to pick 200 Test wickets (in 37 matches). Jadeja has also proven his spinning ability in Indian conditions.
	
</p>


<p align="justify">
However, England are no pushovers. Agreed, they drew their last two Test series against teams from the sub-continent - Bangladesh and Pakistan, yet the world No. 4 cane beat the best on any given day.Among others, they have the talented Joe Root. He has played six matches against India and has scored a whopping 611 runs at an average of 101.83 with his best score being an unbeaten 154.Also, Moeen Ali and Ben Stokes will be the other two players whom England would be hoping to do well. Both did well in the recent series against Bangladesh.Moeen was the joint-highest wicket-taker (11) for England along with Stokes in the series against Bangladesh and his spin will come in very handy for the visitors in Indian conditions.
	
</p>


<p align="justify">
England captain Cook agreed that they would be the underdogs in the series. "It's a big challenge. Any time you play the No. 1 or the No. 2 ranked team in their backyard, who are very comfortable in their conditions, it's a huge task for us. It's a huge challenge to these guys who have not played a huge amount of cricket in the subcontinent as a group," said Cook.However, he was not ready to go down without a fight. "What this (England) side has done over the last couple of years is to exceed expectations in big series. We have played really good cricket in big series. Last year, we went to South Africa and won away from home when they were No. 1. These (India) are different conditions, but being underdogs takes a lot of pressure off us," Cook added.
</p>

