Around 4,000 illegal shops face closure in Jaipur with the civic corporation announcing its crackdown on such shops and slaughterhouses from April. Meat sellers claimed that 950 of these 4,000 shops were authorised, but the corporation did not renew their licences after March 31 last year.

</p>
				    <p align="justify">
A JMC official said the licences could not be renewed as the municipal body had approved a proposal for increasing the licence fee from Rs 10 to Rs 1,000, but the gazette notification had not been issued till date. This despite the directorate of local bodies clearing the file on licence fee hike and sending it back to JMC. "We are not at fault as we filed applications for renewal of licences. However, our applications were not accepted. We will protest against the JMC's drive," said Abdul Raquf Khurshi, president,New Jaipur Meat Association. Sources in the JMC said that initially, action will be taken against illegal slaughterhouses and meat shops. "Shops which had licences in the past will be spared. However, action will be taken against them if slaughtering is not done as per the law," said a senior JMC official. In Haridwar, raids by the administration revealed that out of six shops only three had a valid licence. "The rest three shops were running illegally," said city magistrate Jai Bharat Singh. Haridwar SSP Krishna Kumar V K told TOI: "The action is being taken on complaints by some people's representatives, especially in view of Navratra. While meat shops without licences will face action, those operating them legally with valid documents will not be harassed."



</p>
				    <p align="justify">
In Chhattisgarh, Raipur Municipal Corporation Zone-2 commissioner RK Dongre asked 11 illegal meat shops to down shutters in three days. The shops will be sealed if they fail to do so. The shop owners were found to be polluting the environment by dumping the waste directly on the roadside or into the sewer.



</p>
<p align="justify">
Indore Muncipal Corporation asked one shop to shut down for violating rules. "We had been receiving complaints for some time about the shop. When we reached there we found that the meat was kept in the open. Moreover, the condition of the shop was bad," said an IMC official. Meanwhile, a day after Ranchi Municipal Corporation clamped down on illegal slaughterhouses, most meat shops remained closed in the city.


