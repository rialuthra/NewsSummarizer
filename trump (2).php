				    <p align="justify">It packs extra weight as it comes after Trump has said US help for Nato members — already worried by the threat from Russia in the east — might depend on how much they have paid.
"Americans cannot care more for your children's future security than you do," retired Marine general Mattis said in prepared remarks to defence ministers at Nato headquarters.
"If your nations do not want to see America moderate its commitment to this alliance, each of your capitals need to show support for our common defence."
Mattis called for "milestone dates" this year that would track Nato member contributions.</p>



<p align="justify">Nato Secretary General Jens Stoltenberg denied that Mattis's words amounted to a threat, saying it was a "firm message" and that his colleagues agreed that they would do more to contribute.
"Secretary Mattis conveyed a very firm message to all Allies. That message is about the importance of fair burden-sharing. It reflects a political reality in the United States," Stoltenberg told a press conference.
Currently, only the United States, Britain, Estonia, Greece and Poland have hit or surpassed the two percent figure.
The call for more funding comes as Nato mounts its biggest buildup since the Cold War, strengthening its eastern flank following Russia's annexation of Crimea in 2014.
British Defence Secretary Michael Fallon told reporters that he had called on laggards to at least increase spending annually to "demonstrate good faith."</p>

<p align="justify">Nato said its member countries spent a total of $892 billion in 2015, with the United States accounting for $641 billion. Russian military spending in 2015 rose to 66 billion dollars, the SIPRI institute in Stockholm said.
Despite lambasting some Nato partners' spending, Mattis hailed Nato as the "fundamental bedrock" of transatlantic security as he sought to reassure allies about Trump's commitment to the alliance.</p>


<p align="justify">Since his inauguration, Trump has taken a more orthodox stance on Nato and reaffirmed long-standing US commitment to the alliance that Mattis has previously served.
Trump, who is set to meet Nato leaders in Brussels in May, had previously said the alliance was "obsolete."</p>


