				    <p align="justify">
Gene regulation refers to how and when a gene is activated."The research implies this complex mechanism was present at the evolutionary dawn of multicellular animals and across animal species as far apart as sponges and humans," Dr Tanurdzic said."Until very recently we thought increasing complexity in the animal kingdom was due to an ever-increasing number of genes that encode information about animal development and growth."However, the genomics explosion of the last decade taught us most animals have a similar number of genes encoded in their DNA.</p>



<p align="justify">
"The alternative, and today the prevailing explanation, is that gene regulation is responsible for the evolution of animal diversity."The study was largely undertaken by former UQ PhD student Dr Federico Gaiti, now at Weill Cornell Medicine in New York City, USA.Dr Tanurdzic said a key mechanism responsible for regulating genes in multi-celled organisms was how the DNA was packaged within the genetic material, or genome."DNA associates with special proteins -- called histones -- in the nucleus," he said</p>

<p align="justify">
"Histones can bear certain chemical marks, which in turn determine if the DNA associated with them is going to be turned on or off."Histone marking gone awry is also responsible for some of the more insidious genetic errors, such as when a normal cell becomes cancerous."Our study, which used a Great Barrier Reef sponge, Amphimedon queenslandica, set out to discover if this particular mechanism of gene regulation is present in the oldest multicellular animal lineage -- the sponges," he said."Through analysis of DNA with certain histone marks we determined that histone-based gene regulation is part of the sponge gene regulatory tool kit."As the common ancestor of humans and sponges probably lived 700 million years ago, this implies that gene regulatory complexity relying on histone marks was fundamental for the evolution of animal multicellularity and diverse animal forms and functions."</p>


